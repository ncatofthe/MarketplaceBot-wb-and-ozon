class Config:
    """Класс для управления конфигурацией"""
    
    def __init__(self):
        self.config = self._load_config()
        self.answers = self._load_answers()
    
    def _load_config(self):
        """Загрузка конфигурации из файла"""
        if CONFIG_FILE.exists():
            try:
                with open(CONFIG_FILE, 'r', encoding='utf-8') as f:
                    return json.load(f)
            except (json.JSONDecodeError, OSError, IOError) as e:
                from utils import logger
                logger.warning(f"Ошибка загрузки config.json: {e}")
                return DEFAULT_CONFIG.copy()
        return DEFAULT_CONFIG.copy()
    
    def _load_answers(self):
        """Загрузка шаблонов ответов"""
        if ANSWERS_FILE.exists():
            try:
                with open(ANSWERS_FILE, 'r', encoding='utf-8') as f:
                    return json.load(f)
            except (json.JSONDecodeError, OSError, IOError) as e:
                from utils import logger
                logger.warning(f"Ошибка загрузки answers.json: {e}")
                return DEFAULT_ANSWERS.copy()
        return DEFAULT_ANSWERS.copy()
    
    def save_config(self):
        """Сохранение конфигурации в файл"""
        with open(CONFIG_FILE, 'w', encoding='utf-8') as f:
            json.dump(self.config, f, ensure_ascii=False, indent=4)
    
    def save_answers(self):
        """Сохранение шаблонов ответов"""
        with open(ANSWERS_FILE, 'w', encoding='utf-8') as f:
            json.dump(self.answers, f, ensure_ascii=False, indent=4)
    
    def get(self, section, key=None):
        """Получение значения из конфигурации"""
        if key is None:
            return self.config.get(section, {})
        return self.config.get(section, {}).get(key)
    
    def set(self, section, key, value):
        """Установка значения в конфигурации"""
        if section not in self.config:
            self.config[section] = {}
        self.config[section][key] = value
    
    def get_answer_templates(self):
        """Получение шаблонов ответов"""
        return self.answers


